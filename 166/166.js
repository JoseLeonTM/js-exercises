var container = document.createElement('div');
var headers = [];
for (let letter in songs) {
    let header = document.createElement('h2');
    header.id = letter;
    header.innerText = letter;
    headers.push({ elem: header });
    let songList = document.createElement('ul');
    for (let i = 0; i < songs[letter].length; i++) {
        let song = document.createElement('li');
        song.innerText = songs[letter][i];
        songList.appendChild(song);
    }
    container.appendChild(header);
    container.appendChild(songList);
}
var body = document.getElementsByTagName('body')[0];
body.appendChild(container);
//SAVE THE POSITIONS OF THE HEADERS//
for (let i = 0; i < headers.length; i++) {
    headers[i].h = headers[i].elem.offsetTop;
}
document.addEventListener('scroll', function () {
    //CHECK THE POSITIONS AGAINST THE SCROLL//
    for (let i = 0; i < headers.length; i++) {
        //CHANGE CLASSES//
        if (headers[i].h < body.scrollTop) {
            headers[i].elem.className = 'stick';
        }
        else {
            headers[i].elem.className = '';
        }
    }
}, false);
